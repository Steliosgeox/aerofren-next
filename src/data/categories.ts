// Helper function to get category by slug
export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find(cat => cat.slug === slug);
}

// Helper function to get subcategory by slugs
export function getSubcategoryBySlug(
  categorySlug: string,
  subcategorySlug: string
): { category: Category; subcategory: typeof categories[0]['subcategories'][0] } | undefined {
  const category = getCategoryBySlug(categorySlug);
  if (!category) return undefined;

  const subcategory = category.subcategories.find(sub => sub.slug === subcategorySlug);
  if (!subcategory) return undefined;

  return { category, subcategory };
}

// Get all categories for navigation
export function getAllCategories(): Category[] {
  return categories;
}

// Calculate total product count
export function getTotalProductCount(): number {
  return categories.reduce((total, cat) => total + cat.productCount, 0);
}
